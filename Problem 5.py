#This program computes the MPG of Car

#Askig user to input the total number of miles driven
miles_driven= int(input("How many miles were driven?"))

#Asking the user to input the total number of gallons used
gallons_used= int(input("How many gallons were used?"))

#Calculating the MPG by dividing miles driven and gallons used
MPG= miles_driven / gallons_used

#Printing the result of MPG
print("The total MPG is",MPG)
