#This program converts degrees Fahrenheit into degrees Celsius

#Asking User to input the temperature in degrees Fahrenheit
Fahrenheit= int(input("Please enter the temperature in degrees Fahrenheit: "))

#Calculating Celsius by using the input of Fahrenheit and the Formula below
Celsius=(Fahrenheit-32) * 5/9

#Printing the result of Celsius
print("The temperature in degrees Celsius is",Celsius)